     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $booking = new Booking;
        $booking->username = request('name');
        $booking->usermail = request('mail');
        $booking->user_ph_no = request('ph_no');
        $booking->user_description = request('description');
        $booking->seats = request('seats');
        $booking->time = request('date');
        $booking->status = "sent";
        //$request('time');
        $booking->save();
        return redirect('bookings');
    }

